// Helper function to get the correct asset path
export function getAssetPath(path) {
    const mappedAsset = assetMap[path];
    if (mappedAsset) {
        return mappedAsset;
    }

    // Log missing assets in development
    if (import.meta.env.DEV) {
        console.warn(`Missing asset mapping for: ${path}`);
    }

    // Return a fallback or the original path
    return path;
}
